import './adminkit';

// Gr√°fico de encomendas
document.addEventListener("DOMContentLoaded", function() {
    // var jsonClosedOrdersPerMonth = {!! $jsonClosedOrdersPerMonth !!};
    var jsonClosedOrdersPerMonth = JSON.parse(document.getElementById('jsonClosedOrdersPerMonth').value);

    var orders = jsonClosedOrdersPerMonth.map(function(item) {
        return item.count;
    });

    var ctx = document.getElementById("chartjs-dashboard-line").getContext("2d");
    var gradient = ctx.createLinearGradient(0, 0, 0, 225);
    gradient.addColorStop(0, "rgba(215, 227, 244, 1)");
    gradient.addColorStop(1, "rgba(215, 227, 244, 0)");
    // Line chart
    new Chart(document.getElementById("chartjs-dashboard-line"), {
        type: "line",
        data: {
            labels: ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"],
            datasets: [{
                label: "Encomendas",
                fill: true,
                backgroundColor: gradient,
                borderColor: window.theme.primary,
                data: orders
            }]
        },
        options: {
            maintainAspectRatio: false,
            legend: {
                display: false
            },
            tooltips: {
                intersect: false
            },
            hover: {
                intersect: true
            },
            plugins: {
                filler: {
                    propagate: false
                }
            },
            scales: {
                xAxes: [{
                    reverse: true,
                    gridLines: {
                        color: "rgba(0,0,0,0.0)"
                    }
                }],
                yAxes: [{
                    ticks: {
                        stepSize: 100
                    },
                    display: true,
                    borderDash: [3, 3],
                    gridLines: {
                        color: "rgba(0,0,0,0.0)"
                    }
                }]
            }
        }
    });
});
